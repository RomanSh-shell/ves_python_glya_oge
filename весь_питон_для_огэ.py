# --- ОСНОВЫ: ПЕРЕМЕННЫЕ И ТИПЫ ---

# При объявлении переменной не нужно указывать тип данных
a = 0
#   ↑ a — число int

# Когда хотим вывести результат, используем print()
print(a)
# выведет 0

# Когда мы ожидаем ввод от пользователя, используем input()
# ВАЖНО: input() всегда возвращает СТРОКУ str
a = input()
# Допустим, ввели 52. Сейчас a = "52" (текст)

# Если мы попытаемся сложить текст и число:
# print(a + 3) # БУДЕТ ОШИБКА! Python не умеет складывать слова с цифрами.

# Чтобы превратить строку в число, используем int()
b = int(a)
# Теперь b = 52 (число), и с ним можно вести расчеты

# Лайфхак: можно сразу превращать ввод в число (самый частый вариант):
b = int(input())
#             ↑ да, тут нужны пустые скобки

# Чтобы узнать длину строки (сколько в ней символов/цифр), используем len()
# Но len работает только со строками, поэтому иногда число нужно вернуть в строку через str()
s = str(b)   # превратили 52 обратно в "52"
print(len(s)) # Выведет 2 (два знака)


# --- ЛОГИКА: True и False ---
# Это фундамент для условий if и while.
# Компьютер отвечает на вопросы только "Да" (True) или "Нет" (False).

print(5 > 2)    # Выведет: True (5 больше 2)
print(5 < 2)    # Выведет: False (5 не меньше 2)
print(10 == 10) # Выведет: True (Два равно '==' проверяет равенство!)
print(10 != 5)  # Выведет: True (Не равно '!=')

# Логические связки:
# and (И) — Истина, только если ОБА условия верны
print((5 > 2) and (10 < 20)) # True (и то, и то верно)

# or (ИЛИ) — Истина, если ХОТЯ БЫ ОДНО верно
print((5 < 2) or (100 > 5))  # True (второе верно, значит всё выражение верно)


# --- УСЛОВИЯ (if, elif, else) ---
# Отступы [Tab] в Python критически важны! Они показывают, что код внутри условия.

var = 15 # Допустим, у нас есть число 15

if var > 10:
    print("var точно больше, чем 10.") # Выполнится, так как 15 > 10 — это True
elif var < 10:
    print("var меньше, чем 10.") # elif сработает, только если if выше был False
else:
    print("var равно 10.") # else сработает, если всё, что выше — False

# Сложные условия с and / or
if (var > 10) and (var < 20):
    print("var больше 10 и при этом меньше 20")

if (var > 10) or (var < 0):
    print("var либо больше 10, либо отрицательное")


