# --- ЦИКЛЫ ---

# 1. Цикл for — когда знаем, СКОЛЬКО раз повторить
n = 5
for i in range(n):
    # i — это счетчик, он меняется сам: 0, 1, 2, 3, 4
    print(i)
# резульататом выполнения цикла будет
'''
    0
    1
    2
    3
    4
'''

# 2. Цикл while — когда повторяем, ПОКА условие верно
n = 5
while n > 0:
    print(n)
    n -= 1 # Уменьшаем на 1 каждый раз
# результат:
'''
    4
    3
    2
    1
'''

# --- ШАБЛОНЫ ЗАДАЧ ОГЭ (Задание 15.2) ---
# В ОГЭ всего два типа задач.

# ТИП 1: Известно количество чисел (N)
# Задача: Найти количество двузначных чисел, кратных 2.

n = int(input()) # Сначала вводим количество
count = 0        # Обозначаем переменную ЗА пределами цикла, чтобы после его завершения мы могли обратиться к ней

for i in range(n):
    x = int(input()) # Вводим очередное число
    # Проверяем: чётное (x % 2 == 0) И двузначное (9 < x < 100)
    if (x % 2 == 0) and (9 < x < 100):
        count += 1

print(count)


# ТИП 2: Последовательность заканчивается условным знаком, обчно нулём.
# Задача: Найти СУММУ чисел, которые делятся на 5.

summ = 0 # Обозначаем переменную ЗА пределами цикла

while True: # Бесконечный цикл, пока не остановим
    x = int(input())

    if x == 0:  # Сначала проверяем на ноль (стоп-слово)
        break   # break ломает цикл и выбрасывает нас из него

    if x % 5 == 0: # Если не ноль, проверяем условие задачи
        summ += x  # Прибавляем число к сумме

print(summ)





